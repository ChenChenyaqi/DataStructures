public class Pair {
    public int fist;
    public int second;

    public Pair(int fist, int second) {
        this.fist = fist;
        this.second = second;
    }

    public int getFist() {
        return fist;
    }

    public void setFist(int fist) {
        this.fist = fist;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }
}
